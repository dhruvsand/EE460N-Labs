.ORIG x1200
ADD R6, R6, #-2;
STW R0, R6, #0;PUSH R0
ADD R6, R6, #-2;
STW R1, R6, #0;PUSH R1
ADD R6, R6, #-2;
STW R2, R6, #0;PUSH R2
LEA R0, loc;
LDW R0, R0, #0;LD R0 WITH PTBR
LEA R1, len;
LDW R1, R1, #0;LD R1 WITH LEN
LOOP LDW R2,R0,#0; LD PTE IN R2
AND R2,R2,#-2; MAKE THE R BIT 0
STW R2,R0,#0; ST R2 BACK TO PTE
ADD R0,R0,#2 ;INCREMENT PTBR
ADD R1,R1,#-1; DECREMENT LEN
BRp LOOP
LDW R2, R6, #0;POP R2 BACK
ADD R6, R6, #2;
LDW R1, R6, #0;POP R1 BACK
ADD R6, R6, #2;
LDW R0, R6, #0;POP R0 BACK
ADD R6, R6, #2;
RTI;
loc .FILL x1000;PTBR
len .FILL #128;LEN
.END